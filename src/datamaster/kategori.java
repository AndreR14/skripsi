    private void tabelInputMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelInputMouseClicked
        // TODO add your handling code here:
        simpan.setEnabled(false);
        edit.setEnabled(true);
        hapus.setEnabled(true);
        int i = tabelInput.getSelectedRow();
        if (i == -1) {
            return;
        }
        String tablekodepelanggan = (String) model.getValueAt(i, 0);
        kodeKat.setText(tablekodepelanggan);
        kodeKat.setEnabled(false);

        String tablenamapelanggan = (String) model.getValueAt(i, 1);
        namaKat.setText(tablenamapelanggan);
    }//GEN-LAST:event_tabelInputMouseClicked

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
         if (namaKat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "Nayya Beauty Clinic", JOptionPane.INFORMATION_MESSAGE);
        } else {
            int i = tabelInput.getSelectedRow();
            if (i == -1) {
                return;
            }
            try {
                conn = Koneksi.getKoneksi();
                sql = "UPDATE kategori SET nama_kategori=? WHERE kode_kategori='" + kodeKat.getText() + "'";
                pst = conn.prepareStatement(sql);
                pst.setString(1, namaKat.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "DATA BERHASIL DIRUBAH", "Nayya Beauty Clinic", JOptionPane.INFORMATION_MESSAGE);
                namaKat.requestFocus();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Diubah " + e);
            } finally {
                loadData();
                kodekategori();
                namaKat.setText("");
            }
        }
    }//GEN-LAST:event_editActionPerformed

    private void cariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cariKeyReleased
        // TODO add your handling code here:
         model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            conn = Koneksi.getKoneksi();
            st = conn.createStatement();
            sql = "SELECT * FROM kategori WHERE kode_kategori LIKE '%" + cari.getText() + "%' OR nama_kategori LIKE'%" + cari.getText() + "%'";
            rs = st.executeQuery(sql);
            while (rs.next()) {
                Object[] o = new Object[2];
                o[0] = rs.getString("kode_kategori");
                o[1] = rs.getString("nama_kategori");
                model.addRow(o);
            }
            rs.close();
            st.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_cariKeyReleased


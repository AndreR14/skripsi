    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        if (kodeKat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "Nayya Beauty Clinic", JOptionPane.INFORMATION_MESSAGE);
        } else {
            String tkodekategori = kodeKat.getText();
            String tnamakategori = namaKat.getText();
            try {
                conn = Koneksi.getKoneksi();
                sql = "INSERT INTO kategori VALUES (?, ?)";
                pst = conn.prepareStatement(sql);
                pst.setString(1, tkodekategori);
                pst.setString(2, tnamakategori);
                pst.executeUpdate();
                pst.close();
                JOptionPane.showMessageDialog(null, "DATA BERHASIL DIRSIMPAN", "Nayya Beauty Clinic", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            } finally {
                loadData();
                kodekategori();
                namaKat.setText("");
            }
        }
    }//GEN-LAST:event_simpanActionPerformed


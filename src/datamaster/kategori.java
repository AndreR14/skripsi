    private void loadData() {
        simpan.setEnabled(true);
        hapus.setEnabled(false);
        edit.setEnabled(false);
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            conn = Koneksi.getKoneksi();
            st = conn.createStatement();

            sql = "SELECT * FROM kategori ORDER BY kode_kategori";
            rs = st.executeQuery(sql);
            while (rs.next()) {
                Object[] o = new Object[2];
                o[0] = rs.getString("kode_kategori");
                o[1] = rs.getString("nama_kategori");
                model.addRow(o);
            }
            rs.close();
            st.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan" + e);
        }
    }
     private void kodekategori() {
        try {
            conn = Koneksi.getKoneksi();
            st = conn.createStatement();

            sql = "SELECT kode_kategori FROM kategori ORDER by kode_kategori DESC";
            rs = st.executeQuery(sql);

            if (rs.next()) {
                String ibar = rs.getString("kode_kategori").substring(1);
                System.out.println(ibar);
                String AN = "" + (Integer.parseInt(ibar) + 1);
                System.out.println(AN);
                String Nol = "";

                if (AN.length() == 1) {
                    Nol = "0000";
                } else if (AN.length() == 2) {
                    Nol = "000";
                } else if (AN.length() == 3) {
                    Nol = "00";
                } else if (AN.length() == 4) {
                    Nol = "0";
                } else if (AN.length() == 5) {
                    Nol = "";
                }
                System.out.println("D" + Nol + AN);
                kodeKat.setText("D" + Nol + AN);
                
            } else {
                kodeKat.setText("D00001");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }


    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
     if (JenisSat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA", "Nayya Beauty Clinic", JOptionPane.INFORMATION_MESSAGE);
        } else {
            String tkodesatuan = kodeSat.getText();
            String tnamasatuan = JenisSat.getText();
            try {
                conn = Koneksi.getKoneksi();
                sql = "INSERT INTO satuan VALUES (?, ?)";
                pst = conn.prepareStatement(sql);
                pst.setString(1, tkodesatuan);
                pst.setString(2, tnamasatuan);
                pst.executeUpdate();
                pst.close();
                JOptionPane.showMessageDialog(null, "DATA BERHASIL DISIMPAN", "Nayya Beauty Clinic", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            } finally {
                loadData();
                kodesatuan();
                JenisSat.setText("");
            }
        }
    }//GEN-LAST:event_simpanActionPerformed


    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
         if (JenisSat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA", "Nayya Beauty Clinic", JOptionPane.INFORMATION_MESSAGE);
        } else {
            int i = tabelInput.getSelectedRow();
            if (i == -1) {
                return;
            }
            try {
                conn = Koneksi.getKoneksi();
                sql = "UPDATE satuan SET jenisatuan=? WHERE kodesatuan='" + kodeSat.getText() + "'";
                pst = conn.prepareStatement(sql);
                pst.setString(1, JenisSat.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "DATA BERHASIL DIRUBAH", "Nayya Beauty Clinic", JOptionPane.INFORMATION_MESSAGE);
                JenisSat.requestFocus();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            } finally {
                loadData();
                kodesatuan();
                JenisSat.setText("");
            }
        }
    }//GEN-LAST:event_editActionPerformed

